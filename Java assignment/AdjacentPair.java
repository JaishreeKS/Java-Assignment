import java.util.Scanner;
public class AdjacentPair{
	public static void main(String[] args){
		System.out.println("12"+ " " + "34");

		System.out.println("-----------------------------------------------------");
		System.out.print("1");
		System.out.print("2" + "\t");
		System.out.print("3");
		System.out.print("4" + "\n");

		System.out.println("-----------------------------------------------------");

		int num1 = 12, num2 = 34;
		System.out.printf("%d  %d",num1,num2);
	}
}